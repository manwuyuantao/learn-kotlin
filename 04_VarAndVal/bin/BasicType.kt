//变量的取值范围
fun main(args: Array<String>) {
	var aByte:Byte = Byte.MAX_VALUE
	var bByte:Byte = Byte.MIN_VALUE
	
	println("Byte的最大值："+aByte)
	println("Byte的最小值："+bByte)
	
	
	var aLong:Long = Long.MAX_VALUE
	var bLong:Long = Long.MIN_VALUE
	
	println("Long的最大值："+aLong)
	println("Long的最小值："+bLong)
	
	//Int可以采用二进制的形式进行赋值
	val aInt:Int = 0b0011
	println("aInt的值是："+aInt)
	
	
	
	
	
	
	
}